RETURNS jsonb LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE v_user_id uuid := auth.uid(); v_result jsonb; BEGIN
  IF v_user_id IS NULL THEN RETURN '{}'::jsonb; END IF;
