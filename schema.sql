    GROUP BY m.sender_id
  ) m;
  v_result := jsonb_set(COALESCE(v_result,'{}'), '{total}',
    to_jsonb(COALESCE((SELECT SUM(value::bigint) FROM jsonb_each_text(COALESCE(v_result,'{}'))),0)));
  RETURN v_result;
END;
$$;
